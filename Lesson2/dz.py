# Получаем входные данные
n = int(input("Введите количество школьников (n): "))
k = int(input("Введите количество яблок (k): "))
# Вычисляем количество яблок на школьника и остаток в корзине
apples_per_student = k // n  # Целая часть деления
apples_left = k % n      # Остаток от деления
# Выводим результатprint("Каждому школьнику достанется:", apples_per_student)
print("Яблок останется в корзине:", apples_left)

