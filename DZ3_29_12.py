"""Программа запрашивает ввод, с клавиатуры, целые числа, пока не будет введён 0. Как только будет введён 0 (ноль), программа должна посчитать и вывести на экран:
•	количество введённых чисел (завершающий 0 не считается)
•	их сумму
•	среднее арифметическое всех введённых чисел(не считая завершающего числа 0)
•	определить максимальное и минимальное значение
•	определить количество чётных и не чётных элементов в последовательности"""

count = 0  # Количество введенных чисел
total_sum = 0  # Сумма введенных чисел
even_count = 0  # Количество четных чисел
odd_count = 0  # Количество нечетных чисел
max_value = None # Максимальное значение
min_value = None # Минимальное значение

while True:
    
    num = int(input("Введите число (для завершения введите 0): "))

    # Прерываем цикл, если введен 0
    if num == 0:
        break

    # Увеличиваем количество чисел
    count += 1

    # Добавляем число к общей сумме
    total_sum += num

    # Проверка на четность и нечетность
    if num % 2 == 0:
        even_count += 1
    else:
        odd_count += 1

    # Обновление максимального и минимального значения
    if max_value is None or num > max_value:
        max_value = num
    if min_value is None or num < min_value:
        min_value = num

# Если были введены числа, выводим результаты
if count > 0:
    # Среднее арифметическое
    average = total_sum / count

    # Вывод результатов
    print(f"Количество введенных чисел: {count}")
    print(f"Сумма чисел: {total_sum}")
    print(f"Среднее арифметическое: {average:.2f}")
    print(f"Максимальное число: {max_value}")
    print(f"Минимальное число: {min_value}")
    print(f"Четных чисел: {even_count}")
    print(f"Нечетных чисел: {odd_count}")
else:
    print("Вы не ввели ни одного числа.")
